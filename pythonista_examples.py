# ==================== جداول المرجعية ====================

class ReferenceTables:
    """جداول مرجعية للقيم المثالية"""
    
    @staticmethod
    def power_by_distance():
        """القوة المثالية حسب المسافة"""
        print("""
        ═════════════════════════════════════════
        جدول: القوة المثالية حسب المسافة
        ═════════════════════════════════════════
        """)
        
        distances = [30, 50, 75, 100, 150, 200, 250, 300]
        
        print("\nالمسافة (سم) | القوة المثالية | التقييم")
        print("─" * 45)
        
        for d in distances:
            # الصيغة: Power = 35 + (distance * 0.15)
            power = min(100, 35 + (d * 0.15))
            rating = "ممتاز" if d <= 50 else "جيد" if d <= 150 else "صعب"
            print(f"{d:13} | {power:14.1f} | {rating}")
    
    @staticmethod
    def difficulty_comparison():
        """مقارنة مستويات الصعوبة"""
        print("""
        ═════════════════════════════════════════
        جدول: مقارنة مستويات الصعوبة
        ═════════════════════════════════════════
        """)
        
        levels = [
            ('سهل جداً', 1.5, 'للمبتدئين'),
            ('سهل', 1.2, 'للممارسة الأساسية'),
            ('متوسط', 1.0, 'المستوى الافتراضي'),
            ('صعب', 0.8, 'للتحدي'),
            ('صعب جداً', 0.6, 'للاحترافيين'),
            ('احترافي', 0.4, 'للخبراء فقط')
        ]
        
        print("\nالمستوى        | المعامل | الوصف")
        print("─" * 45)
        
        for name, factor, desc in levels:
            print(f"{name:14} | {factor:6.1f}  | {desc}")
    
    @staticmethod
    def angle_impact():
        """تأثير الزاوية على النسبة"""
        print("""
        ═════════════════════════════════════════
        جدول: تأثير الزاوية على النسبة
        ═════════════════════════════════════════
        """)
        
        angles = [0, 5, 10, 15, 30, 45, 60, 90]
        
        print("\nالزاوية (°) | نسبة الفقدان | النسبة المتبقية")
        print("─" * 45)
        
        for angle in angles:
            loss = abs(angle) / 90 * 50  # نسبة الفقدان
            remaining = 100 - loss
            print(f"{angle:11} | {loss:12.1f}% | {remaining:14.1f}%")

